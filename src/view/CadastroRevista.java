    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (validaRevista()) {
            try {
                Revista revista = new Revista();
                revista.setAno(Integer.parseInt(this.txtAno.getText()));
                revista.setNumero((Integer.parseInt(this.txtNro.getText())));
                revista.setTitulo(this.txtTitulo.getText());
                revista.setVolume(Integer.parseInt(this.txtVolume.getText()));

                RevistaFacade revistaFacade = new RevistaFacade();
                revistaFacade.create(revista);

                JOptionPane.showMessageDialog(rootPane, "Revista cadastrada com sucesso");
                this.limpaCaixasDeTexto();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Erro ao cadastrar revista");
            }
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void limpaCaixasDeTexto() {
        this.txtAno.setText("");
        this.txtNro.setText("");
        this.txtTitulo.setText("");
        this.txtVolume.setText("");
    }

    private boolean validaRevista() {
        if (txtTitulo.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "O título da revista é propriedade obrigatória");
            return false;
        }
        return true;
    }

